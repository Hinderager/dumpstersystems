import type { NextConfig } from "next";

const nextConfig: NextConfig = {
  // Enforce trailing slash consistency (no trailing slash)
  trailingSlash: false,

  // Redirect www to non-www (handles at edge if using Vercel)
  async redirects() {
    return [
      // Redirect www to non-www
      {
        source: '/:path*',
        has: [
          {
            type: 'host',
            value: 'www.dumpstersystems.ai',
          },
        ],
        destination: 'https://dumpstersystems.ai/:path*',
        permanent: true,
      },
      // Redirect HTTP to HTTPS (Vercel handles this automatically, but explicit is good)
      {
        source: '/:path*',
        has: [
          {
            type: 'header',
            key: 'x-forwarded-proto',
            value: 'http',
          },
        ],
        destination: 'https://dumpstersystems.ai/:path*',
        permanent: true,
      },
    ];
  },

  // Security headers
  async headers() {
    return [
      {
        source: '/:path*',
        headers: [
          {
            key: 'X-DNS-Prefetch-Control',
            value: 'on',
          },
          {
            key: 'X-Frame-Options',
            value: 'SAMEORIGIN',
          },
          {
            key: 'X-Content-Type-Options',
            value: 'nosniff',
          },
          {
            key: 'Referrer-Policy',
            value: 'origin-when-cross-origin',
          },
        ],
      },
    ];
  },
};

export default nextConfig;
